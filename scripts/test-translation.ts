console.log("=".repeat(60));
console.log();

const { passed, failed, total, failures } = runTests();
const passRate = ((passed / total) * 100).toFixed(2);

console.log(`📊 总体结果: ${passed}/${total} 通过 (${passRate}%)`);
console.log();

if (failures.length > 0) {
  console.log("❌ 失败用例:");
  console.log("-".repeat(60));
  for (const f of failures) {
    console.log(`  ${f}`);
  }
  console.log();
}

console.log("=".repeat(60));

if (parseFloat(passRate) >= 99) {
  console.log("✅ 翻译准确率达标 (≥99%)");
} else if (parseFloat(passRate) >= 95) {
  console.log("⚠️  翻译准确率接近目标 (≥95%, <99%)");
} else {
  console.log("❌ 翻译准确率不达标 (<95%)");
}

console.log("=".repeat(60));

process.exit(parseFloat(passRate) >= 99 ? 0 : 1);

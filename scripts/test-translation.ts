// ==================== 测试逻辑 ====================

function runTests() {
  let passed = 0;
  let failed = 0;
  const failures: string[] = [];

  for (const [zh, expected] of TEST_CASES) {
    const tokens = segment(zh);
    const matched = tokens.filter((t) => t.matched && t.en);
    const actual = matched.map((t) => t.en).join("");

    if (actual.toLowerCase().includes(expected.toLowerCase())) {
      passed++;
    } else {
      failed++;
      failures.push(`${zh} → 期望 "${expected}", 实际 "${actual || "(无结果)"}"`);
    }
  }

  return { passed, failed, total: TEST_CASES.length, failures };
}

